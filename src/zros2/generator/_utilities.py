"""Shared utilities for code generation — header comment, naming, defaults,
import formatters.

**Zero-regex** — type pattern detection is delegated to the Lark-based
:mod:`._type_grammar` parser."""

import ast
import datetime
import hashlib
import re

from importlib.metadata import version as _pkg_version

from lark import LarkError

from ._type_grammar import parse_type


_PKG_VERSION: str = _pkg_version("zros2")


# ═══════════════════════════════════════════════════════════════════════════
# Header comment
# ═══════════════════════════════════════════════════════════════════════════


def _generated_metadata_stmts(source: str | None = None) -> list[ast.stmt]:
    """Return module-level assignment AST nodes for generated-file metadata.

    Every generated module should carry:

    * ``__generated__ = True`` — indicates the file is machine-generated.
    * ``__generator__ = "zros2-gen vX.Y.Z"`` — identifies the generator.
    * ``__source__ = "..."`` — the original ROS 2 IDL source file (omitted
      when ``source`` is ``None``, e.g. for ``__init__.py`` aggregations).
    """
    stmts: list[ast.stmt] = []
    stmts.append(ast.Assign(
        targets=[ast.Name(id="__generated__")],
        value=ast.Constant(value=True),
    ))
    stmts.append(ast.Assign(
        targets=[ast.Name(id="__generator__")],
        value=ast.Constant(value=f"zros2-gen v{_PKG_VERSION}"),
    ))
    if source is not None:
        stmts.append(ast.Assign(
            targets=[ast.Name(id="__source__")],
            value=ast.Constant(value=source),
        ))
    return stmts


def _header_comment(content: str = "", distro: str = "") -> str:
    """Return a "do not modify" header with timestamp and content SHA1.

    The hash is computed from *content* (everything after the header block).
    If *content* is empty the hash line is omitted (e.g. for the registry
    module which is built once at module load time).
    """
    ts = datetime.datetime.now(datetime.timezone.utc).isoformat(
        timespec="seconds"
    )
    lines = [
        f"# This file is auto-generated by zros2-gen v{_PKG_VERSION}. DO NOT MODIFY.",
    ]
    if content:
        sha = hashlib.sha1(content.encode("utf-8")).hexdigest()
        lines.append(f"# SHA1: {sha}")
    if distro:
        lines.append(f"# ROS 2 distro: {distro}")
    lines.append(f"# Generated at: {ts}")
    lines.append("")
    return "\n".join(lines) + "\n"


